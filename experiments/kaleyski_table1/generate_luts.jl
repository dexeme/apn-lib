using Nemo
using APNLib

include(joinpath(@__DIR__, "table1_data.jl"))

const OUTPUT_PATH = joinpath(@__DIR__, "fixtures", "table1_luts.jl")

function trace_value(field, x, trace_degree::Int, terms::Vector{APNTraceMonomial})
    value = zero(field)

    for trace_term in terms
        coefficient = kaleyski_coefficient(field, trace_term.coefficient_power)
        for i in 0:(trace_degree - 1)
            value += coefficient^(2^i) * x^(trace_term.exponent * 2^i)
        end
    end

    return value
end

function component_value(component::APNMonomial, field, x, x_int::Int, n::Int, generated_luts)
    return kaleyski_coefficient(field, component.coefficient_power) * x^component.exponent
end

function component_value(component::APNAbsoluteTrace, field, x, x_int::Int, n::Int, generated_luts)
    return kaleyski_coefficient(field, component.scale_power) * trace_value(field, x, n, component.terms)
end

function component_value(component::APNRelativeTrace, field, x, x_int::Int, n::Int, generated_luts)
    return kaleyski_coefficient(field, component.scale_power) *
           trace_value(field, x, component.extension_degree, component.terms)
end

function component_value(component::APNReference, field, x, x_int::Int, n::Int, generated_luts)
    definition = KALEYSKI_TABLE1_DEFINITION_BY_EQUATION_KEY[(n, component.id)]
    key = (n, definition.table_id)
    haskey(generated_luts, key) || error("referenced LUT $key has not been generated yet")
    return int_to_field_element(generated_luts[key][x_int + 1], field, n)
end

function generated_lut(case, generated_luts)
    field = GF(2, case.n, "a")
    definition = KALEYSKI_TABLE1_DEFINITION_BY_KEY[(case.n, case.id)]
    lut = Vector{Int}(undef, 2^case.n)

    for x_int in 0:(2^case.n - 1)
        x = int_to_field_element(x_int, field, case.n)
        value = zero(field)

        for component in definition.components
            value += component_value(component, field, x, x_int, case.n, generated_luts)
        end

        lut[x_int + 1] = field_element_to_int(value, case.n)
    end

    return lut
end

function write_lut_entry(io, case, lut)
    println(io, "    ($(case.n), \"$(case.id)\") => [")
    for first_index in 1:16:length(lut)
        last_index = min(first_index + 15, length(lut))
        values = join(lut[first_index:last_index], ", ")
        println(io, "        $values,")
    end
    println(io, "    ],")
end

function main()
    mkpath(dirname(OUTPUT_PATH))

    open(OUTPUT_PATH, "w") do io
        println(io, "# This file is generated by experiments/kaleyski_table1/generate_luts.jl.")
        println(io, "# Do not edit it by hand; edit experiments/kaleyski_table1/table1_data.jl instead.")
        println(io)
        println(io, "const KALEYSKI_TABLE1_GENERATED_LUTS = Dict{Tuple{Int, String}, Vector{Int}}(")

        generated_luts = Dict{Tuple{Int, String}, Vector{Int}}()

        for case in KALEYSKI_TABLE1_GENERATION_CASES
            @info "Generating Kaleyski Table 1 LUT" n = case.n id = case.id equation = kaleyski_table1_equation_id(case.n, case.id) formula = kaleyski_table1_formula(case.n, case.id)
            lut = generated_lut(case, generated_luts)
            generated_luts[(case.n, case.id)] = lut
            write_lut_entry(io, case, lut)
        end

        println(io, ")")
    end

    @info "Wrote generated Kaleyski Table 1 LUTs" path = OUTPUT_PATH
end

main()
